package com.example.haberodev;

public class Tur{
    int id;
    String tur;

    public Tur() {
    }

    public Tur(int id, String tur) {
        this.id = id;
        this.tur = tur;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTur() {
        return tur;
    }

    public void setTur(String tur) {
        this.tur = tur;
    }
}
